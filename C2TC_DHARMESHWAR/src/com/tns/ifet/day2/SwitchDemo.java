package com.tns.ifet.day2;

public class SwitchDemo {

	public static void main(String[] args) {
		int i=10;
		switch (i) {
		case 1:
			System.out.println(1);
			break;
		case 10:
			System.out.println(10);
			break;
		case 100:
			System.out.println(100);
			break;

		default:
			break;
		}
	}

}
