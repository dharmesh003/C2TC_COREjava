package com.tns.ifet.day1;

public class DataTypesDemo {
	public static void main(String[] args) {
		int num1=5/10;
		float num2=9/2;
		double num3=9.0/2.5;
		float num4=5/5.9f;
		System.out.println("Integer Division:"+num1);
		System.out.println("Float Division:"+num2);
		System.out.println("Double Division:"+num3);
		System.out.println("Float_2 Division:"+num4);
		int number=256;
		float percent=number*0.46f;
		System.out.println("Percent:"+number);
		
	}
	

}
