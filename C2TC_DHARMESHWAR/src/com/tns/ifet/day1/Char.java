package com.tns.ifet.day1;

public class Char {

	public static void main(String[] args) {
		char ch='a';
		System.out.println("Single Character:"+ch);
		char ch1=29;
		System.out.println("Number As Character:"+ch1);
		int d='D';
		System.out.println("Integer As Character:"+d);
		char ch3='\u00A7';
		System.out.println("Unicode to Charater:"+ch3);
		char ch4='\u30BC';
		System.out.println("Unicode Representation"+ch4);

	}

}
